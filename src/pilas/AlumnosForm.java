    private void save() {
        int code = ALUMNOS.getLargo() + 1;
        String name = nombreText.getText();
        double[] califications = new double[3];
        
        try {
            califications[0] = Double.parseDouble(cal1Text.getText());
            califications[1] = Double.parseDouble(cal2Text.getText());
            califications[2] = Double.parseDouble(cal3Text.getText());
        } catch (NumberFormatException e) {
            showMessage("Los valores de las calificaciones no son validos.", true);
            System.out.println(e.getLocalizedMessage());
            return;
        }
        
        ALUMNOS.apilar(new Alumno(code, name, califications));
        showMessage("Guardado exitosamente", false);
        // clearInputs();
        updateTable();
    }
    
    private void delete() {
        int selectedRowIdx = tabla.getSelectedRow();
        
        if (selectedRowIdx == -1) {
            showMessage("No hay una fila seleccionada.", true);
            return;
        }
        
        ALUMNOS.eliminar(selectedRowIdx);
        updateTable();
    }
    
    private void pop() {
        ALUMNOS.desapilar();
        updateTable();
    }
    
    private void clearInputs() {
        codigoText.setText(String.valueOf(ALUMNOS.getLargo() + 1));
        nombreText.setText(null);
        cal1Text.setText(null);
        cal2Text.setText(null);
        cal3Text.setText(null);
    }
    
    private void updateTable() {
        DefaultTableModel tableModel = (DefaultTableModel)tabla.getModel();
        tableModel.setRowCount(0);
        ArrayList<Alumno> alumnos = ALUMNOS.toArrayList();
        for (Alumno a : alumnos) {
            tableModel.addRow(new Object[] {
                a.getCodigo(),
                a.getNombre(),
                a.getCalificacion(0),
                a.getCalificacion(1),
                a.getCalificacion(2),
                a.getPromedio()
            });
        }
    }
    
    private void showMessage(String msg, boolean isError) {
        if (isError) {
            JOptionPane.showMessageDialog(this, msg, "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, msg, "Correcto", JOptionPane.INFORMATION_MESSAGE);
        }
    }

