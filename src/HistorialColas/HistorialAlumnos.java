    private void showMessage(String msg, boolean isError) {
        if (isError) {
            JOptionPane.showMessageDialog(this, msg, "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, msg, "Correcto", JOptionPane.INFORMATION_MESSAGE);
        }
    }
    
    private void agregar() {
        int edad = 0;
        String nombre = "";
        
        try {
            edad = Integer.parseInt(edadTxt.getText());
            nombre = nombreTxt.getText();
        } catch (NumberFormatException e) {
            showMessage("Datos invalidos El formato de datos es incorrecto.", true);
            limpiar();
            return;
        }
        ALUMNOS.agregar(new Alumno(nombre, edad));
        updateTable();
        limpiar();
    }
    
    private void limpiar() {
        edadTxt.setText(null);
        nombreTxt.setText(null);
    }
    
