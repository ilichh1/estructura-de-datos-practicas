    private void updateTable() {
        DefaultTableModel tableModel = (DefaultTableModel)tabla.getModel();
        tableModel.setRowCount(0);
        int codigo = 0;
        for (Alumno alumno : ALUMNOS.toArrayList()) {
            codigo++;
            tableModel.addRow(new Object[] {
                codigo,
                alumno.edad,
                alumno.nombre
            });
        }
    }

