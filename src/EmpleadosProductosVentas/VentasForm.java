    private void guardar() {
        int id = getId();
        Producto pr = (Producto)productosCombo.getSelectedItem();
        Vendedor ve = (Vendedor)vendedoresCombo.getSelectedItem();
        ventas.insertarAlFinal(new Venta(id, pr, ve));
        limpiarInputs();
    }
    
    private void siguiente() {
        imprimirLabels(ventas.getSiguiente());
    }
    
    private void anterior() {
        imprimirLabels(ventas.getAnterior());
    }
    
    private void imprimirLabels(Venta venta) {
        int c = venta.getId();
        String v = venta.getVendedor().toString();
        String p = venta.getProducto().toString();
        codigoLabel.setText(c+"");
        productoLabel.setText(p);
        vendedorLabel.setText(v);
    }
    
    private void limpiarInputs() {
        codigoTxt.setText(getId()+"");
        vendedoresCombo.setSelectedItem(null);
        productosCombo.setSelectedItem(null);
    }
    
    private int getId() {
        return ventas.getLargo() + 1;
    }

